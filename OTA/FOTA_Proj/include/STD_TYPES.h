#ifndef _STD_TYPES_H
#define _STD_TYPES_H

/*	Unsigned Type	*/
typedef unsigned char 	   u8;
typedef unsigned short int u16;
typedef unsigned int 	   u32;

/*	Signed Type		*/
typedef signed char 	   s8;
typedef signed short int   s16;
typedef signed int         s32;

/*	Float Type		*/
typedef float              f32;
typedef long               f64;
typedef long double        f96;


/*	STD Macros		*/
#define STD_U8_ON     	    0x01
#define STD_U8_OFF     		0x00


#define STD_U8_HIGH    		0x01
#define STD_U8_LOW     		0x00

#define E_OK    	    	0x01
#define E_NOK   	     	0x00

		
#endif
		
		
		
